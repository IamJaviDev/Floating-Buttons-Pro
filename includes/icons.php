/* ── Frontend: slug → SVG HTML ───────────────────────────────── */
function fbpro_icon_library() {
    $out = [];
    foreach ( _fbpro_icon_data() as $slug => $item ) {
        $out[ $slug ] = $item['svg'];
    }
    return $out;
}

/* ── Admin JS: slug → { label, svg } ────────────────────────── */
function fbpro_icon_admin_data() {
    return _fbpro_icon_data();
}

/* ═══════════════════════════════════════════════════════════════
   RENDER ICON (helper para frontend.php)
   ═══════════════════════════════════════════════════════════════ */
function fbpro_render_icon( $button ) {
    if ( ( $button['icon_type'] ?? 'svg' ) === 'image' && ! empty( $button['icon_image_id'] ) ) {
        $html = wp_get_attachment_image(
            $button['icon_image_id'],
            'thumbnail',
            false,
            [ 'class' => 'fbpro-btn__img', 'aria-hidden' => 'true' ]
        );
        if ( $html ) return $html;
    }

    $icons = fbpro_icon_library();
    $slug  = $button['icon_svg'] ?? 'phone';
    return $icons[ $slug ] ?? $icons['phone'];
}
